void Takt(void)
{
	LCM_OUT0 &= ~LCM_E;
	HAL_Delay(10);
	LCM_OUT0 |= LCM_E;
	HAL_Delay(10);
	LCM_OUT0 &= (~LCM_E);
	HAL_Delay(45);
}

void Write_DC(char data, int rs)
{
	if(rs==1) {
		LCM_OUT0=LCM_RS;
	}
	else {
		LCM_OUT0 &= ~LCM_RS;
	}

	LCM_OUT1 = data;
	Takt();
}
void Cursor (char col, int str)
{
	char addInd;
	if (str ==1) {
	addInd=0x80|col;
	LCM_OUT0 &= ~LCM_RS;
	LCM_OUT1 = addInd;
	Takt();
	}
	else {
		addInd=0x80|0x40|col;
		LCM_OUT0 &= ~LCM_RS;
		LCM_OUT1 = addInd;
		Takt();
	}

}

void WriteProg(void)
{
  Write_DC(0x01, 0);
  Write_DC(0xA8, 1);//Ï
  Write_DC(0x70, 1);//ð
  Write_DC(0x6F, 1);//î
  Write_DC(0xB4, 1);//ã
  Write_DC(0x20, 1);//
  Write_DC(0x32, 1);//1
  data=(0x31);
  Cursor (0x06, 1);
}

void InitializeLCD(void)
{
    LCM_OUT1 &= ~(LCM_MASK);
    HAL_Delay(100);
    LCM_OUT0 &= ~LCM_RS;
    LCM_OUT0 &= ~LCM_RW;
    LCM_OUT0 &= ~LCM_E;

    Takt();
    Write_DC(0x3C, 0);
    Write_DC(0x3C, 0);
    Write_DC(0x0F, 0);
    Write_DC(0x01, 0);
    Write_DC(0x06, 0);
}

